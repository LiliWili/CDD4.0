# receber dois numeros e fazer as 4 operações
num1 = float(input("Digite o primeiro numero: "))
num2 = float(input("Digite o segundo numeor: "))
soma = num1+num2
mult = num1*num2
div = num1/num2
sub = num1-num2
print(f"Os resultados são:\n soma: {soma} \n multiplicação: {mult} \n divisão: {div} \n subtração: {sub}")